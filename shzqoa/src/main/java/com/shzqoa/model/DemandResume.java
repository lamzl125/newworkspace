package com.shzqoa.model;

import java.util.Date;

public class DemandResume {
	private String id;   //'关系表id',
	private String demandSignId;   //'需求分配Id',
	private String customerCode;   //'客户编号',
	private Date bindTime;  //'绑定时间',
	private String bindPeople;    //'绑定人',
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDemandSignId() {
		return demandSignId;
	}
	public void setDemandSignId(String demandSignId) {
		this.demandSignId = demandSignId;
	}
	public String getCustomerCode() {
		return customerCode;
	}
	public void setCustomerCode(String customerCode) {
		this.customerCode = customerCode;
	}
	public Date getBindTime() {
		return bindTime;
	}
	public void setBindTime(Date bindTime) {
		this.bindTime = bindTime;
	}
	public String getBindPeople() {
		return bindPeople;
	}
	public void setBindPeople(String bindPeople) {
		this.bindPeople = bindPeople;
	}
	
}
