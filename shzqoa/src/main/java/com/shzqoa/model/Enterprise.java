package com.shzqoa.model;


public class Enterprise {
	private String enterpriseId;//企业Id
	private String enterpriseName;//企业名称
	private String enterpriseScale;//企业规模
	private String enterpriseProfile;//企业简介
	private String enterpriseURL;//企业网址
	private String enterpriseAddr;//企业地址
	private String remark;//备注
	private Integer enterpriseStatus;//企业状态（1-启用  2-不启用）
	private String abateResource;//失效原因
	
	
	public String getEnterpriseId() {
		return enterpriseId;
	}
	public void setEnterpriseId(String enterpriseId) {
		this.enterpriseId = enterpriseId;
	}
	public String getEnterpriseName() {
		return enterpriseName;
	}
	public void setEnterpriseName(String enterpriseName) {
		this.enterpriseName = enterpriseName;
	}
	public String getEnterpriseScale() {
		return enterpriseScale;
	}
	public void setEnterpriseScale(String enterpriseScale) {
		this.enterpriseScale = enterpriseScale;
	}
	public String getEnterpriseProfile() {
		return enterpriseProfile;
	}
	public void setEnterpriseProfile(String enterpriseProfile) {
		this.enterpriseProfile = enterpriseProfile;
	}
	public String getEnterpriseURL() {
		return enterpriseURL;
	}
	public void setEnterpriseURL(String enterpriseURL) {
		this.enterpriseURL = enterpriseURL;
	}
	public String getEnterpriseAddr() {
		return enterpriseAddr;
	}
	public void setEnterpriseAddr(String enterpriseAddr) {
		this.enterpriseAddr = enterpriseAddr;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Integer getEnterpriseStatus() {
		return enterpriseStatus;
	}
	public void setEnterpriseStatus(Integer enterpriseStatus) {
		this.enterpriseStatus = enterpriseStatus;
	}
	public String getAbateResource() {
		return abateResource;
	}
	public void setAbateResource(String abateResource) {
		this.abateResource = abateResource;
	}
	
}
