package com.shzqoa.model;


public class TrainSchool {
	
	private String trainSchoolId;  //培训学校Id
	private Long trainSchoolLevel;  //培训学校层次
	private Long trainSchoolZone;//培训学校区域Id
	private String trainSchoolName;//培训学校名称
	private String trainSchoolAddr;  // 培训学校地址
	private String trainSchoolURL;//培训学校网址
	private Integer trainSchoolStatus;  //培训学校状态（0-正常  1-废弃）
	private String reMark;  //备注
	
	
	
	
	public String getTrainSchoolId() {
		return trainSchoolId;
	}
	public void setTrainSchoolId(String trainSchoolId) {
		this.trainSchoolId = trainSchoolId;
	}
	public Long getTrainSchoolLevel() {
		return trainSchoolLevel;
	}
	public void setTrainSchoolLevel(Long trainSchoolLevel) {
		this.trainSchoolLevel = trainSchoolLevel;
	}
	public Long getTrainSchoolZone() {
		return trainSchoolZone;
	}
	public void setTrainSchoolZone(Long trainSchoolZone) {
		this.trainSchoolZone = trainSchoolZone;
	}
	public String getTrainSchoolName() {
		return trainSchoolName;
	}
	public void setTrainSchoolName(String trainSchoolName) {
		this.trainSchoolName = trainSchoolName;
	}
	public String getTrainSchoolAddr() {
		return trainSchoolAddr;
	}
	public void setTrainSchoolAddr(String trainSchoolAddr) {
		this.trainSchoolAddr = trainSchoolAddr;
	}
	public Integer getTrainSchoolStatus() {
		return trainSchoolStatus;
	}
	public void setTrainSchoolStatus(Integer trainSchoolStatus) {
		this.trainSchoolStatus = trainSchoolStatus;
	}
	public String getReMark() {
		return reMark;
	}
	public void setReMark(String reMark) {
		this.reMark = reMark;
	}
	public String getTrainSchoolURL() {
		return trainSchoolURL;
	}
	public void setTrainSchoolURL(String trainSchoolURL) {
		this.trainSchoolURL = trainSchoolURL;
	}
	
		
}
