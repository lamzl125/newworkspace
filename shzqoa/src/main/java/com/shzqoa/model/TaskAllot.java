package com.shzqoa.model;

import java.io.Serializable;
import java.util.Date;

public class TaskAllot implements Serializable {

	/** serialVersionUID */
	private static final long serialVersionUID = -7596897267334541979L;
	/** id */
	private String id;
	/** 任务表id */
	private String taskid;
	/** 执行任务人id */
	private String userid;
	/** 任务开始时间 */
	private Date starttime;
	/** 任务结束时间 */
	private Date endtime;
	/** 任务量 */
	private String allotnum;
	/** 状态 (0：未完成，1：已完成) */
	private String status;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTaskid() {
		return taskid;
	}

	public void setTaskid(String taskid) {
		this.taskid = taskid;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public Date getStarttime() {
		return starttime;
	}

	public void setStarttime(Date starttime) {
		this.starttime = starttime;
	}

	public Date getEndtime() {
		return endtime;
	}

	public void setEndtime(Date endtime) {
		this.endtime = endtime;
	}

	public String getAllotnum() {
		return allotnum;
	}

	public void setAllotnum(String allotnum) {
		this.allotnum = allotnum;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
}
