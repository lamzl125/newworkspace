package com.shzqoa.model;

import java.util.Date;

/**
 * 工作日志
 * @author Auser
 *
 */
public class DailyLog {
	private Integer dailylogid; // 工作日志id
	
	private Integer taskid; // 工作任务id
	
	private String publishcontent; // 工作任务内容
	
	private int taskstatus;//任务状态0-未完成  1-已完成  2-处理中 3-已处理  4-已校验
	
	private String userid;  // 工作日志发布人id
	
	private String realname; // 工作日志提交者名字 
	 
	private String logcontent; // 工作日志内容
	
	private String propose; // 问题建议
	
	private Date logdate; //工作日志日期
	
	private int isreply;//是否回复(1-回复 0-未回复)
	
	private String replycontent;//回复内容
	
	private String weixin;//微信号
	private String lastCount;//原来有微信好友人数
	private String addCount;//今日增加好友数
	private String addDynamic;//今日朋友圈动态数
	
	
	
	public String getPublishcontent() {
		return publishcontent;
	}

	public void setPublishcontent(String publishcontent) {
		this.publishcontent = publishcontent;
	}

	public String getRealname() {
		return realname;
	}

	public void setRealname(String realname) {
		this.realname = realname;
	}

	public Integer getDailylogid() {
		return dailylogid;
	}

	public void setDailylogid(Integer dailylogid) {
		this.dailylogid = dailylogid;
	}

	public Integer getTaskid() {
		return taskid;
	}

	public void setTaskid(Integer taskid) {
		this.taskid = taskid;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getLogcontent() {
		return logcontent;
	}

	public void setLogcontent(String logcontent) {
		this.logcontent = logcontent;
	}

	public String getPropose() {
		return propose;
	}

	public void setPropose(String propose) {
		this.propose = propose;
	}

	public Date getLogdate() {
		return logdate;
	}

	public void setLogdate(Date logdate) {
		this.logdate = logdate;
	}

	public int getIsreply() {
		return isreply;
	}

	public void setIsreply(int isreply) {
		this.isreply = isreply;
	}

	public String getReplycontent() {
		return replycontent;
	}

	public void setReplycontent(String replycontent) {
		this.replycontent = replycontent;
	}

	public int getTaskstatus() {
		return taskstatus;
	}

	public void setTaskstatus(int taskstatus) {
		this.taskstatus = taskstatus;
	}

	public String getWeixin() {
		return weixin;
	}

	public void setWeixin(String weixin) {
		this.weixin = weixin;
	}

	public String getLastCount() {
		return lastCount;
	}

	public void setLastCount(String lastCount) {
		this.lastCount = lastCount;
	}

	public String getAddCount() {
		return addCount;
	}

	public void setAddCount(String addCount) {
		this.addCount = addCount;
	}

	public String getAddDynamic() {
		return addDynamic;
	}

	public void setAddDynamic(String addDynamic) {
		this.addDynamic = addDynamic;
	}

	
	
	
}




