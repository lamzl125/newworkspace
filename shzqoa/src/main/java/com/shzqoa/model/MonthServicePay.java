package com.shzqoa.model;

import java.io.Serializable;
import java.util.Date;

public class MonthServicePay implements Serializable {
	private static final long serialVersionUID = 1L;
	
	private String id;//简历月服务费Id
	private String customerProjectPayId;//客户项目服务费Id	
	private Integer yearIndex;//年
	private Integer monthIndex;//月
	private Integer payStatus;//付款状态(1-未付款  2-已付款）
	private Double servicePay;//月服务费用
	private Double subPay;//扣除费用
	private String subContent;//扣除费用说明
	private Double realPay;//实际付款费用
	private String addPeople;//添加人
	private Date addTime;//添加时间
	private String updatePeople;//更新人
	private Date updateTime;//更新时间
	private String remark;//备注	
	private Date estimatePayDate;//预计付款时间
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCustomerProjectPayId() {
		return customerProjectPayId;
	}
	public void setCustomerProjectPayId(String customerProjectPayId) {
		this.customerProjectPayId = customerProjectPayId;
	}
	public Integer getYearIndex() {
		return yearIndex;
	}
	public void setYearIndex(Integer yearIndex) {
		this.yearIndex = yearIndex;
	}
	public Integer getMonthIndex() {
		return monthIndex;
	}
	public void setMonthIndex(Integer monthIndex) {
		this.monthIndex = monthIndex;
	}
	public Integer getPayStatus() {
		return payStatus;
	}
	public void setPayStatus(Integer payStatus) {
		this.payStatus = payStatus;
	}
	public Double getServicePay() {
		return servicePay;
	}
	public void setServicePay(Double servicePay) {
		this.servicePay = servicePay;
	}
	public Double getSubPay() {
		return subPay;
	}
	public void setSubPay(Double subPay) {
		this.subPay = subPay;
	}
	public String getSubContent() {
		return subContent;
	}
	public void setSubContent(String subContent) {
		this.subContent = subContent;
	}
	public Double getRealPay() {
		return realPay;
	}
	public void setRealPay(Double realPay) {
		this.realPay = realPay;
	}
	public String getAddPeople() {
		return addPeople;
	}
	public void setAddPeople(String addPeople) {
		this.addPeople = addPeople;
	}
	public Date getAddTime() {
		return addTime;
	}
	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}
	public String getUpdatePeople() {
		return updatePeople;
	}
	public void setUpdatePeople(String updatePeople) {
		this.updatePeople = updatePeople;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Date getEstimatePayDate() {
		return estimatePayDate;
	}
	public void setEstimatePayDate(Date estimatePayDate) {
		this.estimatePayDate = estimatePayDate;
	}
	
	
}
