package com.shzqoa.model;


/**
 * 工作日志简历数量
 * @author Auser
 *
 */
public class DailyLogResumeCount {
	private String id;
	
	private Integer dailylogid; // 工作日志id
	
	private Integer resumesourceid; // 简历来源id
	
	private Integer count; // 数量
	
	private String remark;  // 备注

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getDailylogid() {
		return dailylogid;
	}

	public void setDailylogid(Integer dailylogid) {
		this.dailylogid = dailylogid;
	}

	public Integer getResumesourceid() {
		return resumesourceid;
	}

	public void setResumesourceid(Integer resumesourceid) {
		this.resumesourceid = resumesourceid;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
	
	
	
}




