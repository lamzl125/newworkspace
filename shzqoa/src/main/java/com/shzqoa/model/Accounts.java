package com.shzqoa.model;

import java.util.Date;

public class Accounts {
	
	private Integer aid;  //账号Id,
	private String account;  //账户编号,
	private Date strarttime;  //周期开始时间,
	private Date endtime;  //周期结束时间,
	private Integer quantity;  //  简历份数,
	private String accountCity;//简历地址
	private Integer resumesourceid; //'简历来源'
	public Date getStrarttime() {
		return strarttime;
	}
	public void setStrarttime(Date strarttime) {
		this.strarttime = strarttime;
	}
	public Date getEndtime() {
		return endtime;
	}
	public void setEndtime(Date endtime) {
		this.endtime = endtime;
	}
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	public Integer getAid() {
		return aid;
	}
	public void setAid(Integer aid) {
		this.aid = aid;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getAccountCity() {
		return accountCity;
	}
	public void setAccountCity(String accountCity) {
		this.accountCity = accountCity;
	}
	public Integer getResumesourceid() {
		return resumesourceid;
	}
	public void setResumesourceid(Integer resumesourceid) {
		this.resumesourceid = resumesourceid;
	}
	
}
