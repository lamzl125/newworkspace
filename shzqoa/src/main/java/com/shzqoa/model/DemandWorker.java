package com.shzqoa.model;



public class DemandWorker {
	
	private String workerId;  //工人Id
	private String demandId;  //普工需求Id
	private String demandWorkerId;  //需求普工Id
	
	
	public String getWorkerId() {
		return workerId;
	}
	public void setWorkerId(String workerId) {
		this.workerId = workerId;
	}
	public String getDemandId() {
		return demandId;
	}
	public void setDemandId(String demandId) {
		this.demandId = demandId;
	}
	public String getDemandWorkerId() {
		return demandWorkerId;
	}
	public void setDemandWorkerId(String demandWorkerId) {
		this.demandWorkerId = demandWorkerId;
	}
	
	
}
